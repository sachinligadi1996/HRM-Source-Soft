<?php
if (isset($_GET['emp_id'])) {
    // Sanitize the employee ID to prevent SQL injection
    $employee_id = mysqli_real_escape_string($conn, $_GET['emp_id']);

    // Fetch images associated with the specific employee
    $query = "SELECT uploadfile FROM assign_task WHERE emp_id = '$employee_id'";
    $result = mysqli_query($conn, $query);

    if ($result) {
        if (mysqli_num_rows($result) > 0) {
            $imagePaths = array();
            while ($row = mysqli_fetch_assoc($result)) {
                $imagePaths[] = $row['uploadfile'];
            }

            // Open a div for centering images
            echo'<main class="mt-5 pt-3">';
            echo '<div class="center-image">';

            // Output the image content with HTML img tags
            foreach ($imagePaths as $image_path) {
                echo '<img src="' . $image_path . '" alt="Employee Image" width="95%" height="600"><br>';
            }

            // Close the centering div
            echo '</div>';
            echo'</main>';
        } else {
            echo "No images found for the specified employee.";
        }
    } else {
        echo "Error in the database query: " . mysqli_error($conn);
    }
} else {
    echo "Invalid parameters.";
}

